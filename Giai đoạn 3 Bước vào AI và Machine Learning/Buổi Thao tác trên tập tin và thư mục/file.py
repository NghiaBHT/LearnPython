# Opent File
# fh = opent(filepath, mode)
# filepath: đường dẫn của file
# mode: chế độ mở có 1 số chế độ:
#   - r : mở để đọc nội dung(mặc định)
#   - w : mở để ghi nội dung
#   - a : mở để ghi nội dung ở cuối file
#   - r+ : mở để đọc và ghi, Con trỏ ở đầu file.
#   - w+ : mở để đọc và ghi, Ghi đè nếu file đã tồn tại
#   - a+ : mở để đọc và thêm vào cuối file. Con trỏ nằm ở cuối file. Nếu file chưa tồn tại thì tạo file mới để ghi
