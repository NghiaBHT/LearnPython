# Các toán tử số học trong Python
x = 7
y = 2
print(x + y, x - y, x * y, x / y, x // y, x % y, x ** y)
# Kết quả: 9 5 14 3.5 3 1 49

# Các toán tử so sánh trong Python
print(x == y, x != y, x > y, x < y, x >= y, x <= y)
# Kết quả: False True True False True False

# Các toán tử logic trong Python
print(x > 5 and y < 5, x > 5 or y < 5, not (x > 5 and y < 5))
# Kết quả: True True False

# Các toán tử gán trong Python
x += 2  # x = x + 2
print(x)  # Kết quả: 9

x -= 2  # x = x - 2
print(x)  # Kết quả: 7

x *= 2  # x = x * 2
print(x)  # Kết quả: 14




